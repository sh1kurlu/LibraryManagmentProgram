package week03.datetime;

public class CustomDate {
    private int month, day, year;

    public CustomDate(int month, int day, int year){
        this.month = month;
        this.day = day;
        this.year = year;
    }

    public int getMonth(){
        return month;
    }

    public int getDay(){
        return day;
    }

    public int getYear(){
        return year;
    }
    


}
